namespace SyncTrip.Core.Enums;

/// <summary>
/// Types de véhicules supportés par l'application.
/// </summary>
public enum VehicleType
{
    /// <summary>
    /// Voiture (automobile).
    /// </summary>
    Car = 1,

    /// <summary>
    /// Moto ou scooter (deux-roues motorisé).
    /// </summary>
    Motorcycle = 2,

    /// <summary>
    /// Camion ou véhicule utilitaire.
    /// </summary>
    Truck = 3,

    /// <summary>
    /// Van ou fourgonnette.
    /// </summary>
    Van = 4,

    /// <summary>
    /// Camping-car ou motorhome.
    /// </summary>
    Motorhome = 5
}
